# Built In Functions

# Get a number from user and display
num = float(input('Please enter a number: '))
absolute = abs(num)
print('The absolute value is', absolute)

# Find the miminum of two numbers
num1 = float(input('Please enter a number: '))
num2 = float(input('Please enter a number: '))
minimumValue = min(num1, num2)
print('The lowest of the two numbers is', minimumValue)

# Round a number
num = float(input("Please enter a decimal number: "))
roundedValue = round(num)
print('The value round is', roundedValue)